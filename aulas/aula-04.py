# Tipos de dados (inteiros e decimais)

# Os números inteiros em Python são da classe int
numero_inteiro = 10
print(type(numero_inteiro)) # Utilizamos a função type() para verificar o tipo de um dado
print(numero_inteiro)

# Já os números decimais são da classe float
numero_decimal = 8.3
print(type(numero_decimal)) # Utilizamos a função type() para verificar o tipo de um dado
print(numero_decimal)

'''
Ambos os tipos (int e float) são extremamente importates para operações aritméticas e devem ser utilizados
de acordo com o contexto do programa.
'''
